function template_handle_spreadsheet($reporter)
{
    @ob_clean();

    require_once 'modules/Reports/Matrix/MatrixReportData.php';
    $model = new MatrixReportData($reporter);

    require_once 'include/PHPExcel.php';
    $document = new PHPExcel();
    $sheet = $document->setActiveSheetIndex(0);

    require_once 'modules/Reports/View/ExcelView.php';
    $view = new ExcelView();
    $view->render($model, $sheet);

    require_once 'include/PHPExcel/IOFactory.php';
    $writer = PHPExcel_IOFactory::createWriter($document, 'Excel5');

    $filename = get_report_filename($reporter->name, 'xls');
    header('Content-Type: application/vnd.ms-excel');
    header('Content-Disposition: attachment;filename="' . $filename . '"');
    header('Cache-Control: max-age=0');
    $writer->save('php://output');
}
