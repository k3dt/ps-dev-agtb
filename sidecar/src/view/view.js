(function(app) {

    /**
     * Base View class. Use {@link View.ViewManager} to create instances of views.
     *
     * @class View.View
     * @alias SUGAR.App.layout.View
     */
    app.view.View = app.view.Component.extend({
        initialize: function(options) {
            _.bindAll(this);

            /**
             * The context is used to determine what the current focus is
             * (includes a model, collection, and module)
             * @cfg {Core.Context}
             */
            this.context = options.context || app.controller.context;

            /**
             * Name of the View
             * @cfg {String}
             */
            this.name = options.name;

            /**
             * Id of the View. Autogenerated if not specified.
             * @cfg {String}
             * @type {String}
             */
            this.id = options.id || this.getID();

            this.$el.addClass("view " + this.name);

            /**
             * Template to render
             * @cfg {Function}
             */
            this.template = options.template || app.template.get(this.name, (this.context ? this.context.get("module") : null));

            /**
             * Metadata
             * @cfg {Object}
             */
            this.meta = options.meta;

            /**
             * Dictionary of field widgets.
             *
             * - keys: field IDs (sfuuid)
             * - value: instances of `app.view.Field` class
             */
            this.fields = {};
        },

        /**
         * Bind render to data
         * @method
         * @param {Object} data Model or collection to bind to
         */
        bindData: function(data) {
            data.on('reset', this.render);
        },

        /**
         * Bind this view to listen to the given context's event.
         * @param {Core.Context} context
         */
        bind: function(context) {

        },

        /**
         * Renders the view onto the page. (should be overriden by subclasses instead of the formal render function if they need more advanced rendering or do not use a template)
         * @protected
         * @method
         */
        _render: function() {
            // Bad templates can cause a JS error that we want to catch here
            if (this.template) {
                try {
                    this.$el.html(this.template(this));
                } catch (e) {
                    app.logger.error("Failed to render '" + this.name + "' view.\n" + e.message);
                    // TODO: trigger app event to render an error message
                }
            }
        },

        /**
         * Renders the view onto the page.
         * See Backbone.View documentation for details.
         * @return {Object} Reference to this view.
         */
        render: function() {
            if (app.acl.hasAccess(this.name, this.context.get("model"))) {
                this._render();
                // Render will create a placeholder for sugar fields. we now need to populate those fields
                _.each(this.fields, function(sf) {
                    sf.setElement(this.$el.find("span[sfuuid='" + sf.sfid + "']"));
                    try {
                        sf.render();
                    } catch (e) {
                        app.logger.error("Failed to render field '" + sf.name + "' on '" + this.name + "' view.\n" + e.message);
                        // TODO: trigger app event to render an error message
                    }
                }, this);
            } else {
                app.logger.info("Current user does not have access to this module view.");
                //TODO trigger app event to notify user about no access
            }

            return this;
        },

        /**
         * Extracts the fields from the metadata for directly related views/panels
         * TODO: Possibly refactor
         * @return {Array} List of fields used on this view
         */
        getFields: function() {
            var fields = [];
            if (this.meta && this.meta.panels) {
                _.each(this.meta.panels, function(panel) {
                    fields = fields.concat(_.pluck(panel.fields, 'name'));
                });
            }

            return _.filter(_.uniq(fields), function(value) {
                return value;
            });
        },

        /**
         * Returns the html id of this view's el. Will create one if one doesn't exist.
         * @return {String} id of this view.
         */
        getID: function() {
            return (this.id || this.options.module || "") + "_" + this.name;
        }
    });


})(SUGAR.App);